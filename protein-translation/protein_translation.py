from textwrap import wrap

amino = {'AUG':'Methionine',
    'UUU': 'Phenylalanine',
    'UUC': 'Phenylalanine',
    'UUA': 'Leucine',
    'UUG': 'Leucine',
    'UCU': 'Serine',
    'UCC': 'Serine',
    'UCA': 'Serine',
    'UCG': 'Serine',
    'UAU': 'Tyrosine',
    'UAC': 'Tyrosine',
    'UGU': 'Cysteine',
    'UGC': 'Cysteine',
    'UGG': 'Tryptophan',
    'UAA': 'STOP',
    'UAG': 'STOP',
    'UGA': 'STOP'}

def proteins(strand):
    codons = wrap(strand, 3)
    sequence = []
    for x in codons:
        protein = amino[x]
        if protein == 'STOP':
            break
        sequence.append(protein)
    return sequence